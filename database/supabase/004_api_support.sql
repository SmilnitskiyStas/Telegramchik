create table if not exists public.notification_settings (
  id bigint generated by default as identity primary key,
  store_id bigint not null references public.stores (id) on update cascade on delete cascade,
  enabled boolean not null default false,
  chat_id text not null default '',
  send_time time not null default '08:00',
  days_before integer not null default 7 check (days_before >= 1 and days_before <= 30),
  last_sent_date date,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now()),
  constraint uq_notification_settings_store_id unique (store_id)
);


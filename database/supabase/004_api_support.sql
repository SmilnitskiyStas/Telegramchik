create or replace function public.api_upsert_product_and_batch(
  p_article text,
  p_barcode text,
  p_product_name text,
  p_units_of_measurement text,
  p_category text,
  p_store_id bigint,
  p_quantity integer,
  p_received_at date,
  p_expiry_date date,
  p_intake_note text,
  p_actor_user_id bigint
)
returns setof public.api_products_v
language plpgsql
security definer
set search_path = public
as $$
declare
  v_product_id bigint;
  v_batch_id bigint;
begin
  insert into public.products (
    article,
    barcode,
    product_name,
    units_of_measurement,
    category
  )
  values (
    coalesce(nullif(btrim(p_article), ''), p_barcode),
    btrim(p_barcode),
    btrim(p_product_name),
    coalesce(nullif(btrim(p_units_of_measurement), ''), 'pcs'),
    coalesce(nullif(btrim(p_category), ''), 'Без категорії')
  )
  on conflict (barcode) do update
  set
    article = excluded.article,
    product_name = excluded.product_name,
    units_of_measurement = excluded.units_of_measurement,
    category = excluded.category
  returning id into v_product_id;

  insert into public.product_batches (
    product_id,
    store_id,
    quantity,
    expiry_date,
    delivery_date,
    intake_note,
    created_by_user_id,
    updated_by_user_id
  )
  values (
    v_product_id,
    p_store_id,
    greatest(coalesce(p_quantity, 0), 0),
    p_expiry_date,
    p_received_at,
    p_intake_note,
    p_actor_user_id,
    p_actor_user_id
  )
  on conflict (product_id, store_id, expiry_date) do update
  set
    quantity = excluded.quantity,
    delivery_date = excluded.delivery_date,
    intake_note = excluded.intake_note,
    updated_by_user_id = excluded.updated_by_user_id
  returning id into v_batch_id;

  return query
  select *
  from public.api_products_v
  where id = v_batch_id;
end;
$$;

create or replace function public.api_update_product_batch_status(
  p_batch_id bigint,
  p_check_status public.check_status,
  p_actor_user_id bigint,
  p_action_taken public.action_taken default null,
  p_action_note text default null
)
returns setof public.api_products_v
language plpgsql
security definer
set search_path = public
as $$
begin
  update public.product_batches
  set
    check_status = p_check_status,
    checked_by_user_id = p_actor_user_id,
    checked_at = timezone('utc', now()),
    action_taken = p_action_taken,
    action_note = p_action_note,
    updated_by_user_id = p_actor_user_id
  where id = p_batch_id;

  return query
  select *
  from public.api_products_v
  where id = p_batch_id;
end;
$$;


create or replace function public.api_get_notification_settings(p_store_id bigint default public.current_store_id())
returns table (
  enabled boolean,
  chat_id text,
  send_time text,
  days_before integer,
  last_sent_date text
)
language sql
stable
as $$
  select
    ns.enabled,
    ns.chat_id,
    to_char(ns.send_time, 'HH24:MI') as send_time,
    ns.days_before,
    case
      when ns.last_sent_date is null then null
      else to_char(ns.last_sent_date, 'YYYY-MM-DD')
    end as last_sent_date
  from public.notification_settings ns
  where ns.store_id = p_store_id
  limit 1;
$$;

create or replace function public.api_upsert_notification_settings(
  p_store_id bigint,
  p_enabled boolean,
  p_chat_id text,
  p_send_time text,
  p_days_before integer,
  p_last_sent_date date default null
)
returns public.notification_settings
language plpgsql
security definer
set search_path = public
as $$
declare
  v_row public.notification_settings;
begin
  insert into public.notification_settings (
    store_id,
    enabled,
    chat_id,
    send_time,
    days_before,
    last_sent_date
  )
  values (
    p_store_id,
    coalesce(p_enabled, false),
    coalesce(p_chat_id, ''),
    coalesce(p_send_time, '08:00')::time,
    greatest(coalesce(p_days_before, 7), 1),
    p_last_sent_date
  )
  on conflict (store_id) do update
  set
    enabled = excluded.enabled,
    chat_id = excluded.chat_id,
    send_time = excluded.send_time,
    days_before = excluded.days_before,
    last_sent_date = coalesce(excluded.last_sent_date, public.notification_settings.last_sent_date)
  returning * into v_row;

  return v_row;
end;
$$;


create index if not exists idx_product_batches_store_status on public.product_batches (store_id, check_status);
create index if not exists idx_product_batches_expiry_date on public.product_batches (expiry_date);
create index if not exists idx_product_batches_notified on public.product_batches (notified, notified_days);
create index if not exists idx_activity_log_user_id on public.activity_log (user_id);
create index if not exists idx_activity_log_batch_id on public.activity_log (batch_id);
create index if not exists idx_activity_log_product_id on public.activity_log (product_id);
create index if not exists idx_activity_log_store_id on public.activity_log (store_id);
create index if not exists idx_notification_log_batch_id on public.notification_log (batch_id);
create index if not exists idx_notification_log_product_id on public.notification_log (product_id);
